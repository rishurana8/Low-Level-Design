package org.rishudesign.com.Designs.ZoomCar;

public class User {
    String userId;
    String name;
    String email;
    String phoneNumber;
    String driverLicenseNumber;

    public User(String userId, String name, String email, String phoneNumber, String driverLicenseNumber) {
        this.userId = userId;
        this.name = name;
        this.email = email;
        this.phoneNumber = phoneNumber;
        this.driverLicenseNumber = driverLicenseNumber;
    }
}
